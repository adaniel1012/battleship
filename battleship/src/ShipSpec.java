public class ShipSpec {
    private final String name; //spec's name
    private final int length; // spec's length in cells
    public ShipSpec(String name, int length) {
        this.name = name; //save name
        this.length = length; //save length
    }
    public String name() { return name; } //getter
    public int length() { return length; } //getter 
}
    
    

